    private void UpdateRotationBlendOut(int boneIndex)
    {
        float deltaTime = updateTimeDelta * timeScale;
        BoneRotation boneRotation = FindBoneRotation(boneIndex);
        if (boneRotation != null)
        {
            boneRotation.rotation.Get(out vec3 axis, out float deltaAngle);
            boneRotation.blendOutAmount += (deltaTime * boneRotation.blendOutDegreesPerSecond) / (deltaAngle);
            boneRotation.blendOutAmount = MathLib.Min(boneRotation.blendOutAmount, 1.0f);
            boneRotation.rotation = Unigine.MathLib.Slerp(boneRotation.rotation, quat.IDENTITY, boneRotation.blendOutAmount);
        }
    }
