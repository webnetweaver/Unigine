    private void UpdateBoneTransforms()
    {

        float delta = updateTimeDelta * timeScale;
        for (int i = boneInterpolatedRotations.Count - 1; i >= 0; i--)
        {
            BoneInterpolatedRotation boneInterpolatedRotation = boneInterpolatedRotations[i];
            if (boneInterpolatedRotation.blendAmount == 1.0f && boneInterpolatedRotation.blendTarget == 1.0f)
            {
                ApplyBoneRotation(boneInterpolatedRotation.boneIndex, boneInterpolatedRotation.currentRotation);
                continue;
            }
            else
            {
                //Log.Warning($"\nblendamount: {boneInterpolatedRotation.blendAmount}\n");
                if (boneInterpolatedRotation.blendAmount < 1.0f && boneInterpolatedRotation.blendTarget == 1.0f)
                {
                    //updating interpolate blend
                    //blending from currentRotation to targetRotation
                    float deltaBlend = (boneInterpolatedRotation.degreesPerSecond * delta) / boneInterpolatedRotation.angularDistance;
                    boneInterpolatedRotation.blendAmount = (float)Unigine.MathLib.Min(boneInterpolatedRotation.blendAmount + deltaBlend, 1.0f);
                    ApplyBoneInterpolatedRotation(boneInterpolatedRotation);
                    if (boneInterpolatedRotation.blendAmount == 1.0f)
                    {
                        onBoneInterpolateDone.Run(boneInterpolatedRotation.boneName);
                    }
                }
                else if (boneInterpolatedRotation.blendAmount > 0.0f && boneInterpolatedRotation.blendTarget == 0.0f)
                {
                    //removing interpolate
                    //blending from currentRotation to transform to layer rotation

                    float deltaBlend = (boneInterpolatedRotation.degreesPerSecond * delta) / boneInterpolatedRotation.angularDistance;
                    boneInterpolatedRotation.blendAmount = (float)Unigine.MathLib.Max(boneInterpolatedRotation.blendAmount - deltaBlend, 0.0f);
                    ApplyBoneInterpolatedRotation(boneInterpolatedRotation);
                    if (boneInterpolatedRotation.blendAmount == 0.0f)
                    {
                        boneInterpolatedRotations.Remove(boneInterpolatedRotation);
                        onClearBoneInterpolateDone.Run(boneInterpolatedRotation.boneName);
                    }
                }

            }
        }

        for (int i = boneRotations.Count - 1; i >= 0; i--)
        {
            BoneRotation boneRotation = boneRotations[i];
            if (boneRotation.blendOutDegreesPerSecond == 0.0f)
            {
                ApplyBoneRotation(boneRotation.boneIndex, boneRotation.rotation);
            }
            else
            {
                UpdateRotationBlendOut(boneRotation.boneIndex);
                ApplyBoneRotation(boneRotation.boneIndex, boneRotation.rotation);
                if (boneRotation.blendOutAmount == 1.0f)
                {
                    boneRotations.Remove(boneRotation);
                    onClearBoneRotationSmoothDone.Run(meshSkinned.GetBoneName(boneRotation.boneIndex));
                }
            }
        }
    }

    private void ApplyBoneInterpolatedRotation(BoneInterpolatedRotation boneInterpolatedRotation)
    {
        //mat4 currentXform = meshSkinned.GetLayerBoneTransform(0, boneInterpolatedRotation.boneIndex);
        quat currentRotation;
        //Log.Warning("\ngetting here "+ boneInterpolatedRotation.blendAmount);
        //quat remainingQuat;
        if (boneInterpolatedRotation.blendTarget == 1.0f)
        {
            currentRotation = Unigine.MathLib.Slerp(boneInterpolatedRotation.startRotation, boneInterpolatedRotation.targetRotation, boneInterpolatedRotation.blendAmount);
            //remainingQuat = boneInterpolatedRotation.currentRotation - boneInterpolatedRotation.targetRotation;
        }
        else
        {
            currentRotation = Unigine.MathLib.Slerp(quat.IDENTITY, boneInterpolatedRotation.targetRotation, boneInterpolatedRotation.blendAmount);
            //remainingQuat = boneInterpolatedRotation.currentRotation - meshSkinned.GetLayerBoneRotation(0, boneInterpolatedRotation.boneIndex);
        }
        //remainingQuat.Get(out vec3 axis, out float angle);
        //boneInterpolatedRotation.angularDistance = angle;
        boneInterpolatedRotation.currentRotation = currentRotation;
        meshSkinned.SetLayerBoneRotation(0, boneInterpolatedRotation.boneIndex, meshSkinned.GetLayerBoneRotation(0, boneInterpolatedRotation.boneIndex) * currentRotation);
    }

    private void ApplyBoneRotation(int boneIndex, quat targetRotation)
    {
        //mat4 finalXform = meshSkinned.GetLayerBoneTransform(0, boneIndex) * new mat4(targetRotation);
        meshSkinned.SetLayerBoneRotation(0, boneIndex, meshSkinned.GetLayerBoneRotation(0, boneIndex) * targetRotation);
    }
    private BoneInterpolatedRotation FindBoneRotationInterpolate(int boneIndex)
    {
        foreach (BoneInterpolatedRotation bxform in boneInterpolatedRotations)
        {
            if (bxform.boneIndex == boneIndex)
                return bxform;
        }
        return null;
    }

    private BoneRotation FindBoneRotation(int boneIndex)
    {
        foreach (BoneRotation bxform in boneRotations)
        {
            if (bxform.boneIndex == boneIndex)
                return bxform;
        }
        return null;
    }

    public void RotateBoneTo(string boneName, quat targetRotation)
    {
        int boneIndex = meshSkinned.FindBone(boneName);
#if DEBUG
        if (boneIndex == -1)
        {
            Log.Message($"RotateBoneTo: bone {boneName} not found\n");
            return;
        }

        var boneInterpolatedRotation = FindBoneRotationInterpolate(boneIndex);
        if (boneInterpolatedRotation != null)
        {
            Log.Message($"RotateBoneTo: bone {boneName} is being interpolated and cannot be rotated\n");
            return;
        }
#endif
        BoneRotation boneRotation = FindBoneRotation(boneIndex);
        quat startRotation = meshSkinned.GetLayerBoneRotation(0, boneIndex);
        if (boneRotation == null)
        {
            boneRotation = new BoneRotation()
            {
                boneIndex = boneIndex,
                boneName = boneName,
                rotation = MathLib.Inverse(startRotation) * targetRotation,
            };
            boneRotations.Add(boneRotation);
        }
        else
        {
            boneRotation.rotation = MathLib.Inverse(startRotation) * targetRotation;
            boneRotation.blendOutDegreesPerSecond = 0.0f;
        }
    }
    public void RotateBoneBy(string boneName, quat deltaRotation)
    {
        int boneIndex = meshSkinned.FindBone(boneName);
#if DEBUG
        if (boneIndex == -1)
        {
            Log.Message($"RotateBoneBy: bone {boneName} not found\n");
            return;
        }

        var boneInterpolatedRotation = FindBoneRotationInterpolate(boneIndex);
        if (boneInterpolatedRotation != null)
        {
            Log.Message($"RotateBoneBy: bone {boneName} is being interpolated and cannot be rotated\n");
            return;
        }
#endif
        BoneRotation boneRotation = FindBoneRotation(boneIndex);
        if (boneRotation == null)
        {
            boneRotation = new BoneRotation()
            {
                boneIndex = boneIndex,
                rotation = deltaRotation,
                blendOutDegreesPerSecond = 0.0f,
                boneName = boneName
            };
            boneRotations.Add(boneRotation);
        }
        else
        {
            boneRotation.rotation *= deltaRotation;
            boneRotation.blendOutDegreesPerSecond = 0.0f;
        }
    }
    public void RotateBoneByInterpolated(string boneName, quat deltaQuat, float speed = 20.0f)
    {
        int boneIndex = meshSkinned.FindBone(boneName);
#if DEBUG
        if (boneIndex == -1)
        {
            Log.Message($"RotateBoneByInterpolated: bone {boneName} not found\n");
            return;
        }
        BoneRotation boneRotation = FindBoneRotation(boneIndex);
        if (boneRotation != null)
        {
            Log.Message($"RotateBoneByInterpolated: bone {boneName} is being rotated and cannot be interpolated\n");
            return;
        }
#endif
        BoneInterpolatedRotation boneInterpolatedRotation = FindBoneRotationInterpolate(boneIndex);
        //quat startRotation;
        if (boneInterpolatedRotation == null)
        {
            boneInterpolatedRotation = new BoneInterpolatedRotation()
            {
                boneIndex = boneIndex,
                boneName = boneName,
                startRotation = quat.IDENTITY,
                targetRotation = deltaQuat
            };
            boneInterpolatedRotations.Add(boneInterpolatedRotation);
        }
        else
        {
            if (boneInterpolatedRotation.blendAmount > 0.0f && boneInterpolatedRotation.blendAmount < 1.0f)
                return;
            boneInterpolatedRotation.startRotation = boneInterpolatedRotation.currentRotation;
            boneInterpolatedRotation.targetRotation = boneInterpolatedRotation.currentRotation * deltaQuat;
        }

        boneInterpolatedRotation.blendTarget = 1.0f;
        boneInterpolatedRotation.blendAmount = 0.0f;
        boneInterpolatedRotation.degreesPerSecond = MathLib.Abs(speed);
        (deltaQuat).Get(out vec3 axis, out float angle);
        boneInterpolatedRotation.angularDistance = MathLib.Abs(angle);
    }

    public void RotateBoneToInterpolated(string boneName, quat targetQuat, float speed = 20.0f)
    {
        int boneIndex = meshSkinned.FindBone(boneName);
#if DEBUG
        if (boneIndex == -1)
        {
            Log.Message($"RotateBoneToInterpolated: bone {boneName} not found\n");
            return;
        }
        BoneRotation boneRotation = FindBoneRotation(boneIndex);
        if (boneRotation != null)
        {
            Log.Message($"RotateBoneToInterpolated: bone {boneName} is being rotated and cannot be interpolated\n");
            return;
        }

#endif
        BoneInterpolatedRotation boneInterpolatedRotation = FindBoneRotationInterpolate(boneIndex);
        quat startRotation;
        startRotation = meshSkinned.GetLayerBoneRotation(0, boneIndex);
        if (boneInterpolatedRotation == null)
        {
            boneInterpolatedRotation = new BoneInterpolatedRotation()
            {
                boneName = boneName,
                boneIndex = boneIndex,
                startRotation = quat.IDENTITY,
                targetRotation = MathLib.Inverse(startRotation) * targetQuat
            };
            boneInterpolatedRotations.Add(boneInterpolatedRotation);
        }
        else
        {
            if (boneInterpolatedRotation.blendAmount > 0.0f && boneInterpolatedRotation.blendAmount < 1.0f)
                return;
            boneInterpolatedRotation.startRotation = boneInterpolatedRotation.currentRotation;
            boneInterpolatedRotation.targetRotation = boneInterpolatedRotation.currentRotation * MathLib.Inverse(startRotation) * targetQuat;
        }

        boneInterpolatedRotation.blendTarget = 1.0f;
        boneInterpolatedRotation.blendAmount = 0.0f;
        boneInterpolatedRotation.degreesPerSecond = MathLib.Abs(speed);
        (MathLib.Inverse(startRotation) * targetQuat).Get(out vec3 axis, out float angle);
        boneInterpolatedRotation.angularDistance = MathLib.Abs(angle);
    }

