    private void CheckTrigger(AnimationTrigger animationTrigger, int layerNum, int totalFrames, bool reversing = false)
    {
        float animationTime = meshSkinned.GetLayerFrame(layerNum) % totalFrames;

        if (animationTrigger.currentFrame == -1.0f)
        {
            animationTrigger.currentFrame = animationTime;
            return;
        }
        if (!reversing)
        {
            if (animationTime > animationTrigger.currentFrame)
            {
                if (animationTrigger.currentFrame < animationTrigger.frame && animationTrigger.frame < animationTime)
                {
                    if (animationTrigger.callback.Enabled)
                        animationTrigger.callback.Run();
                }
            }
        }
        else
        {
            if (animationTime > 0.0f)
            {
                if (animationTime < animationTrigger.currentFrame)
                {
                    if (animationTrigger.currentFrame > animationTrigger.frame && animationTrigger.frame > animationTime)
                    {
                        if (animationTrigger.callback.Enabled)
                            animationTrigger.callback.Run();
                    }
                }
            }

        }
        animationTrigger.currentFrame = animationTime;
    }
