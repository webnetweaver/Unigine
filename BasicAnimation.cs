    private float timeScale = 1.0f;
    private float updateTime = 0.0f;
    private float lastUpdateTime = 0.0f;
    private float updateTimeDelta = 0.0f;

    //names
    private string animationName = null;
    private string previousAnimationName = null;

    //layers
