    void Init()
    {
        meshSkinned = node as ObjectMeshSkinned;
        if (!meshSkinned)
            return;
        InitAnimations();
        
        if (processType == AnimationProcessTypes.CustomRate)
        {
            if (processRate > 0)
            {
                updateTime = (float)(1.0f / processRate);
                lastUpdateTime = updateTime;//force initial animation update
            }
            else
            {
                Log.Warning($"Blend tree fps must be greater than 0");
                processType = AnimationProcessTypes.EveryFrame;
            }
        }
    }

    private void UpdateSyncThread()
    {
        if (!meshSkinned)
            return;

        if (timeScale < MathLib.EPSILON)
            return;

        if (processType == AnimationProcessTypes.CustomRate)
        {
            lastUpdateTime += Game.IFps;
            if (lastUpdateTime > updateTime)
            {
                updateTimeDelta = lastUpdateTime;
                lastUpdateTime = 0.0f;
            }
            else
            {
                return;
            }
        }
        else
        {
            updateTimeDelta = Game.IFps;
        }

        preAnimtationUpdate.Run(this);

        if (animationName != null)
            UpdateAnimations();

        if (boneInterpolatedRotations.Count > 0 || boneRotations.Count > 0)
            UpdateBoneTransforms();

        postAnimationUpdate.Run(this);
    }

    #region animations
    private void InitAnimations()
    {
        meshSkinned.AddLayer();
        layerNum = meshSkinned.NumLayers - 1;
        meshSkinned.AddLayer();
        prevLayerNum = meshSkinned.NumLayers - 1;
    }

    private void UpdateAnimations()
    {
        if (animationName != null)
        {
            bool blending = !(blendTarget == 1.0 && blendAmount == 1.0);
            bool prevCurBlending = !(prevCurBlendAmount == 1.0);
            AdvanceFrame();
            if (prevCurBlending)
            {
                UpdatePrevCurBlend();
                if (prevCurBlendAmount < 1.0f)
                    ApplyPrevCur();
                else
                {
                    meshSkinned.ClearLayer(prevLayerNum);
                    onAnimationChangeComplete.Run(animationName, previousAnimationName);
                }
            }

            if (blending)
            {
                UpdateBlend();
            }
            Apply();

            //check animation triggers
            if (animationTriggers.Count > 0)
            {
                NamedAnimation animation = GetNamedAnimation(animationName);
                if (animation.frameNumber != -1)
                {
                    return;
                }
                foreach (AnimationTrigger animationTrigger in animationTriggers)
                {
                    if (animationTrigger.animation == animationName)
                    {
                        int totalFrames = GetTotalFrames(animation);
                        CheckTrigger(animationTrigger, layerNum, totalFrames, !pingPongForward);
                    }
                }
            }
        }
    }

    private float ComputeFrame(NamedAnimation animation)
    {
        float delta = updateTimeDelta * animationFPS * timeScale;
        if (animation.endType == AnimationEndTypes.Loop)
            return meshSkinned.GetLayerFrame(layerNum) + delta;
        else if (animation.endType == AnimationEndTypes.Hold)
        {
            float nextFrame = meshSkinned.GetLayerFrame(layerNum) + delta;
            int totalFrames = GetTotalFrames(animation);
            if (nextFrame >= totalFrames)
                return totalFrames;
            else
                return nextFrame;
        }
        else//Ping pong
        {
            int totalFrames = GetTotalFrames(animation);
            if (pingPongForward)
            {
                float nextFrame = meshSkinned.GetLayerFrame(layerNum) + delta;
                if (nextFrame >= totalFrames)
                {
                    //ping pong animation type has crossed over the length of the animation
                    if (nextFrame < meshSkinned.GetLayerFrame(layerNum) + totalFrames)
                    {
                        //crossover length is less than the length of the animation
                        //reverse direction starting from the end of the animation
                        pingPongForward = false;
                        return totalFrames - (nextFrame - totalFrames);
                    }
                    else
                    {
                        //crossover length is greater than the length of the animation cycle
                        //this is unlikely but possible depending on the relative animation length and configured updateTimeDelta
                        //see how many animation cycles should have occurred since the last frame
                        //this indicates how many additional "bounces" occurred after crossing the animation length
                        //an odd number of additional bounces means new frame starts at the beginning by an amount (nextframe - currentframe) % animationLength
                        //an even number of additional bounces means reverse direction starting at the end by an amount (nextframe - currentframe) % animationLength
                        float cycles = (nextFrame - meshSkinned.GetLayerFrame(layerNum)) / totalFrames;
                        int bounces = (int)MathLib.Round(cycles, MidpointRounding.ToZero);
                        if (bounces % 2 != 0)
                        {
                            //odd number of additional bounces
                            return (nextFrame - meshSkinned.GetLayerFrame(layerNum)) % totalFrames;
                        }
                        else
                        {
                            //even number of additional bounces
                            pingPongForward = false;
                            return totalFrames - ((nextFrame - meshSkinned.GetLayerFrame(layerNum)) % totalFrames);
                        }
                    }
                }
                else
                    return nextFrame;
            }
            else
            {
                float nextFrame = meshSkinned.GetLayerFrame(layerNum) - delta;
                if (nextFrame <= 0)
                {
                    //reversing the ping pong animation has crossed over the zero frame of the animation
                    if (nextFrame > meshSkinned.GetLayerFrame(layerNum) - totalFrames)
                    {
                        //crossover length is less than the length of the animation (nextFrame is negative)
                        //reverse direction to forward, make nextFrame positive, and start at the beginning by amount nextFrame
                        pingPongForward = true;
                        return MathLib.Negative(nextFrame);
                    }
                    else
                    {
                        //crossover length is greater than the length of the animation cycle
                        //this is unlikely but possible depending on the relative animation length and configured updateTimeDelta
                        //see how many animation cycles should have occurred since the last frame
                        //this indicates how many additional "bounces" occurred after crossing the animation length
                        //an odd number of additional bounces means new frame starts at the end by an amount (nextframe - currentframe) % animationLength
                        //an even number of additional bounces means reverse direction starting from the end by an amount (nextframe - currentframe) % animationLength
                        float cycles = (nextFrame - meshSkinned.GetLayerFrame(layerNum)) / totalFrames;
                        int bounces = (int)MathLib.Round(MathLib.Negative(cycles), MidpointRounding.ToZero);
                        if (bounces % 2 != 0)
                        {
                            //odd number of additional bounces
                            return totalFrames - (MathLib.Negative(nextFrame - meshSkinned.GetLayerFrame(layerNum)) % totalFrames);
                        }
                        else
                        {
                            //even number of additional bounces
                            pingPongForward = true;
                            return MathLib.Negative(nextFrame - meshSkinned.GetLayerFrame(layerNum)) % totalFrames;
                        }

                    }
                }
                else
                    return nextFrame;
            }
        }
    }

    private int GetTotalFrames(NamedAnimation animation)
    {
        if (animation.from == -1 && animation.to == -1)
            return meshSkinned.GetLayerNumFrames(layerNum);
        int startFrame = animation.from == -1 ? 0 : animation.from;
        int endFrame = animation.to == -1 ? meshSkinned.GetLayerNumFrames(layerNum) : animation.to - 1;
        return endFrame - startFrame;
    }

    private void AdvanceFrame()
    {
        if (animationName != null)
        {
            NamedAnimation animation = GetNamedAnimation(animationName);
            if (animation.frameNumber == -1)
            {
                float frame = ComputeFrame(animation);
                meshSkinned.SetLayerFrame(layerNum, frame, animation.from, animation.to);
            }
            else
            {
                meshSkinned.SetLayerFrame(layerNum, animation.frameNumber);
            }
        }
    }

    private void UpdateBlend()
    {
        float lerpAmount = blendSpeedFactor * updateTimeDelta * timeScale; //globalLerpAmount;

        if (blendTarget > blendAmount)
        {
            blendAmount = (float)Unigine.MathLib.Min(blendAmount + lerpAmount, 1.0f);
            if (blendAmount == 1.0f)
            {
                onBlendedIn.Run(animationName);
            }
        }

        else if (blendTarget < blendAmount)
        {
            blendAmount = (float)Unigine.MathLib.Max(blendAmount - lerpAmount, 0.0f);
            if (blendAmount == 0.0f)
            {
                if (animationName != null)
                {
                    ResetTriggersForAnimation(animationName);

                    meshSkinned.SetLayerFrame(layerNum, 0.0f);
                    animationName = null;
                    onBlendedOut.Run(animationName);
                }
            }
        }
    }

    private void UpdatePrevCurBlend()
    {
        float lerpAmount = blendSpeedFactor * updateTimeDelta * timeScale; //globalLerpAmount;
        blendAmount = (float)Unigine.MathLib.Min(blendAmount + lerpAmount, 1.0f);
    }

    private void ApplyPrevCur()
    {
        meshSkinned.LerpLayer(layerNum, prevLayerNum, layerNum, prevCurBlendAmount);
    }

    private void Apply()
    {
        meshSkinned.LerpLayer(0, 0, layerNum, blendAmount);
    }

    private void ResetTriggersForAnimation(string animationTriggerName)
    {
        if (animationTriggers.Count == 0)
            return;
        AnimationTrigger result = animationTriggers.Find(animationTrigger => animationTrigger.name == animationTriggerName);
        if (result != null)
            result.currentFrame = -1.0f;
    }

    public void AddAnimationTrigger(string triggerName, string animationName, float time, EventDelegate cb)
    {
        AnimationTrigger result = animationTriggers.Find(animationTrigger => animationTrigger.name == triggerName);
        if (result == null)
        {
            AnimationTrigger animationTrigger = new AnimationTrigger() { name = triggerName, animation = animationName, frame = time, currentFrame = -1.0f };
            animationTrigger.callback.Connect(cb);
            animationTriggers.Add(animationTrigger);
            return;
        }
        Log.Warning($"AddAnimationTrigger: trigger name {triggerName} is already taken");
    }

    public void RemoveAnimationTrigger(string triggerName, EventDelegate cb)
    {
        AnimationTrigger result = animationTriggers.Find(animationTrigger => animationTrigger.name == triggerName);
        if (result != null)
        {
            if (!result.callback.Disconnect(cb))
            {
                Log.Warning($"RemoveAnimationTrigger: EventDelegate Disconnect was unsuccessful while removing {triggerName}");
            }
            animationTriggers.Remove(result);
        }
    }

    public void SuspendAnimationTrigger(string triggerName)
    {
        AnimationTrigger result = animationTriggers.Find(animationTrigger => animationTrigger.name == triggerName);
        if (result != null)
        {
            result.callback.Enabled = false;
            return;
        }
        Log.Warning($"SuspendAnimationTrigger: trigger name {triggerName} not found");
    }

    public void ResumeAnimationTrigger(string triggerName)
    {
        AnimationTrigger result = animationTriggers.Find(animationTrigger => animationTrigger.name == triggerName);
        if (result != null)
        {
            result.callback.Enabled = true;
            return;
        }
        Log.Warning($"ResumeAnimationTrigger: trigger name {triggerName} not found");
    }

    public void SetAnimation(string newAnimationName, float speedFactor = 3.75f)
    {
        //aleways reset triggers regardless of whether we're switching animations or not
        if (animationName != null && (animationTriggers.Count > 0))
        {
            ResetTriggersForAnimation(animationName);
        }

        if (newAnimationName == animationName)
        {
            //resetting the same animation
            //copy bone states to prev layer, reset prevCur blend and reset current frame
            meshSkinned.CopyLayer(prevLayerNum, 0);
            prevCurBlendAmount = 0.0f;
            NamedAnimation animation = GetNamedAnimation(newAnimationName);
            if (animation.frameNumber == -1)
                meshSkinned.SetLayerFrame(layerNum, 0);
            blendSpeedFactor = speedFactor;
            return;
        }
        foreach (NamedAnimation animation in animations)
        {
            if (animation.name == newAnimationName)
            {
                if (animationName != null)
                {
                    //switching animations
                    //initialize prevCur blend value and swap current and prev layer numbers
                    var tempLayerNum = prevLayerNum;
                    prevLayerNum = layerNum;
                    layerNum = tempLayerNum;
                    previousAnimationName = animationName;
                    prevCurBlendAmount = 0.0f;
                    animationFPS = animation.framesPerSecond;
                }
                else
                {
                    //setting a new animation
                    //copy current bone states to prev layer, reset prevCur blend and reset current frame
                    blendAmount = 0.0f;
                    blendTarget = 1.0f;
                    meshSkinned.CopyLayer(prevLayerNum, 0);
                    prevCurBlendAmount = 0.0f;
                    animationFPS = animation.framesPerSecond;
                }

                //requested animation found
                //load new animation into layer, reset frame
                animationName = newAnimationName;
                meshSkinned.SetLayerAnimationFilePath(layerNum, animation.asset.Path);
                meshSkinned.SetLayerFrame(layerNum, 0);
                blendSpeedFactor = speedFactor;
                return;
            }
        }
        Log.Warning($"\nSetAnimation(): {animationName} not found");
    }

    public void SetFrame(float frame)
    {
        if (animationName != null)
        {
            meshSkinned.SetLayerFrame(layerNum, MathLib.Max(frame, meshSkinned.GetLayerNumFrames(layerNum)));
        }

    }

