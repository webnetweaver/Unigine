    private float animationFPS = 30.0f;

    //animation type
    private bool pingPongForward = true;

    private List<AnimationTrigger> animationTriggers = new List<AnimationTrigger>();

    private List<BoneInterpolatedRotation> boneInterpolatedRotations = new List<BoneInterpolatedRotation>();
