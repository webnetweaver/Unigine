    private void InitFullBody()
    {
        //dont call meshSkinned.SetLayer() because this layer does not always contribute to the final blend
        //if used, it will interpolate and copy to base layer: meshSkinned.LerpLayer(0, 0, layer, fullBodyBlendAmount);
        meshSkinned.AddLayer();
        fullBodyLayerNum = meshSkinned.NumLayers - 1;
        meshSkinned.AddLayer();
        prevFullBodyLayerNum = meshSkinned.NumLayers - 1;
        meshSkinned.AddLayer();
        layeredFullBodyLayerNum = meshSkinned.NumLayers - 1;
        meshSkinned.AddLayer();
        prevLayeredFullBodyLayerNum = meshSkinned.NumLayers - 1;
    }
    private void UpdateFullBody()
    {
        bool fullBodyPresent = fullBodyAnimationName != null;
        bool fullBodyLayerPresent = layeredFullBodyAnimationName != null;
        if (fullBodyPresent || fullBodyLayerPresent)
        {
            bool fullBodyBlending = !(fullBodyBlendTarget == 1.0f && fullBodyBlendAmount == 1.0f);
            bool fullBodyLayerBlending = !(layeredFullBodyBlendTarget == 1.0f && layeredFullBodyBlendAmount == 1.0f);
            bool fullBodyPrevCurBlending = !(prevCurFullBodyBlendAmount == 1.0f);
            bool fullBodyLayeredPrevCurBlending = !(prevCurLayeredFullBodyBlendAmount == 1.0f);
            AdvanceFullBodyFrame();//meshSkinned.SetLayerFrame() updated advance to update base and oneshot and start blending out when done
            if (fullBodyPrevCurBlending)
            {
                UpdatePrevCurFullBodyBlend();
                if (prevCurFullBodyBlendAmount < 1.0f)
                    ApplyFullBodyPrevCur();//LerpLayer between prev and curr FullBody layer into fullBodyLayer
                else
                {
                    meshSkinned.ClearLayer(prevFullBodyLayerNum);
                    onFullBodyAnimationChangeComplete.Run(fullBodyAnimationName, previousfullBodyAnimationName);
                }
            }

            if (fullBodyLayerPresent)
            {
                if (fullBodyLayeredPrevCurBlending)
                {
                    UpdatePrevCurLayeredFullBodyBlend();
                    if (prevCurLayeredFullBodyBlendAmount < 1.0f)
                        ApplyLayeredFullBodyPrevCur();//LerpLayer between prev and curr layered FullBody layer into layeredFullBodyLayer
                    else
                    {
                        meshSkinned.ClearLayer(prevLayeredFullBodyLayerNum);
                        onLayeredFullBodyAnimationChangeComplete.Run(layeredFullBodyAnimationName, previousLayeredFullBodyAnimationName);
                    }
                }
                if (fullBodyLayerBlending)
                    UpdateLayeredFullBodyBlend();//update layeredFullBodyBlendAmount potentially meshSkinned.SetLayerFrame(0) if oneshot is blended completely out//oneshot blend is updating

                ApplyFullBodyLayered();//LerpLayer layeredFullBody layer into fullBodyLayer
            }

            if (fullBodyBlending || (fullBodyLayerPresent && fullBodyLayerBlending))
            {
                UpdateFullBodyBlend(fullBodyBlending, (fullBodyLayerPresent && fullBodyLayerBlending));//update fullBodyBlendAmount potentially meshSkinned.SetLayerFrame(0) if base animation is blended completely out
            }
            ApplyFullBody();//blend blended full body into base layer

            //check animation triggers
            if (fbAnimationTriggers.Count > 0)
            {
                string animationToCheck;
                int layerToCheck;
                if (layeredFullBodyAnimationName != null)
                {
                    animationToCheck = layeredFullBodyAnimationName;
                    layerToCheck = layeredFullBodyLayerNum;
                }
                else
                {
                    NamedAnimation fullBodyAnimation = GetNamedAnimation(fullBodyAnimationName, FullBodyAnimations);
                    if (fullBodyAnimation.frameNumber != -1)
                    {
                        return;
                    }
                    animationToCheck = fullBodyAnimationName;
                    layerToCheck = fullBodyLayerNum;
                }

                foreach (AnimationTrigger animationTrigger in fbAnimationTriggers)
                {
                    if (animationTrigger.animation == animationToCheck)
                    {
                        NamedAnimation fbAnimation = GetNamedAnimation(animationToCheck, FullBodyAnimations);
                        int totalFrames = GetTotalFullBodyFrames(fbAnimation, layerToCheck);
                        CheckTrigger(animationTrigger, layerToCheck, totalFrames, !fullBodyPingPongForward);
                    }
                }
            }
        }
    }
    private void AdvanceFullBodyFrame()
    {
        float frame;
        if (fullBodyAnimationName != null)
        {
            NamedAnimation fullBodyAnimation = GetNamedAnimation(fullBodyAnimationName, FullBodyAnimations);
            if (fullBodyAnimation.frameNumber == -1)
            {
                frame = ComputeFullBodyFrame(fullBodyAnimation);
                meshSkinned.SetLayerFrame(fullBodyLayerNum, frame, fullBodyAnimation.from, fullBodyAnimation.to);
            }
            else
            {
                meshSkinned.SetLayerFrame(fullBodyLayerNum, fullBodyAnimation.frameNumber);
            }
        }
        if (layeredFullBodyAnimationName != null)
        {
            NamedAnimation layeredFullBodyAnimation = GetNamedAnimation(fullBodyAnimationName, FullBodyAnimations);
            float delta = updateTimeDelta * fullBodyFPS * timeScale;
            frame = meshSkinned.GetLayerFrame(layeredFullBodyLayerNum) + delta;
            int lastFrame = GetTotalFullBodyFrames(layeredFullBodyAnimation, layeredFullBodyLayerNum);
            if (lastFrame > frame)
                meshSkinned.SetLayerFrame(layeredFullBodyLayerNum, frame, layeredFullBodyAnimation.from, layeredFullBodyAnimation.to);
            else
            {
                //end of layered animation, set to the last frame and start blending out
                meshSkinned.SetLayerFrame(layeredFullBodyLayerNum, lastFrame, layeredFullBodyAnimation.from, layeredFullBodyAnimation.to);
                layeredFullBodyBlendTarget = 0.0f;
            }
        }
    }
    private float ComputeFullBodyFrame(NamedAnimation fullBodyAnimation)
    {
        float delta = updateTimeDelta * timeScale * fullBodyFPS;
        if (fullBodyAnimation.endType == AnimationEndTypes.Loop)
            return meshSkinned.GetLayerFrame(fullBodyLayerNum) + delta;
        else if (fullBodyAnimation.endType == AnimationEndTypes.Hold)
        {
            float nextFrame = meshSkinned.GetLayerFrame(fullBodyLayerNum) + delta;
            int totalFrames = GetTotalFullBodyFrames(fullBodyAnimation, fullBodyLayerNum);
            if (nextFrame >= totalFrames) //(nextFrame >= meshSkinned.GetLayerNumFrames(fullBodyLayerNum))
                return totalFrames;// meshSkinned.GetLayerNumFrames(fullBodyLayerNum);
            else
                return nextFrame;
        }
        else//Ping pong
        {
            int totalFrames = GetTotalFullBodyFrames(fullBodyAnimation, fullBodyLayerNum);
            if (fullBodyPingPongForward)
            {
                float nextFrame = meshSkinned.GetLayerFrame(fullBodyLayerNum) + delta;
                if (nextFrame >= totalFrames)
                {
                    //ping pong animation type has crossed over the length of the animation
                    if (nextFrame < meshSkinned.GetLayerFrame(fullBodyLayerNum) + totalFrames)
                    {
                        //crossover length is less than the length of the animation
                        //reverse direction starting from the end of the animation
                        fullBodyPingPongForward = false;
                        return totalFrames - (nextFrame - totalFrames);
                    }
                    else
                    {
                        //crossover length is greater than the length of the animation cycle
                        //this is unlikely but possible depending on the relative animation length and configured updateTimeDelta
                        //see how many animation cycles should have occurred since the last frame
                        //this indicates how many additional "bounces" occurred after crossing the animation length
                        //an odd number of additional bounces means new frame starts at the beginning by an amount (nextframe - currentframe) % animationLength
                        //an even number of additional bounces means reverse direction starting at the end by an amount (nextframe - currentframe) % animationLength
                        float cycles = (nextFrame - meshSkinned.GetLayerFrame(fullBodyLayerNum)) / totalFrames;
                        int bounces = (int)MathLib.Round(cycles, MidpointRounding.ToZero);
                        if (bounces % 2 != 0)
                        {
                            //odd number of additional bounces
                            return (nextFrame - meshSkinned.GetLayerFrame(fullBodyLayerNum)) % totalFrames;
                        }
                        else
                        {
                            //even number of additional bounces
                            fullBodyPingPongForward = false;
                            return totalFrames - ((nextFrame - meshSkinned.GetLayerFrame(fullBodyLayerNum)) % totalFrames);
                        }
                    }
                }
                else
                    return nextFrame;
            }
            else
            {
                float nextFrame = meshSkinned.GetLayerFrame(fullBodyLayerNum) - delta;
                if (nextFrame <= 0)
                {
                    //reversing the ping pong animation has crossed over the zero frame of the animation
                    if (nextFrame > meshSkinned.GetLayerFrame(fullBodyLayerNum) - totalFrames)
                    {
                        //crossover length is less than the length of the animation (nextFrame is negative)
                        //reverse direction to forward, make nextFrame positive, and start at the beginning by amount nextFrame
                        fullBodyPingPongForward = true;
                        return MathLib.Negative(nextFrame);
                    }
                    else
                    {
                        //crossover length is greater than the length of the animation cycle
                        //this is unlikely but possible depending on the relative animation length and configured updateTimeDelta
                        //see how many animation cycles should have occurred since the last frame
                        //this indicates how many additional "bounces" occurred after crossing the animation length
                        //an odd number of additional bounces means new frame starts at the end by an amount (nextframe - currentframe) % animationLength
                        //an even number of additional bounces means reverse direction starting from the end by an amount (nextframe - currentframe) % animationLength
                        float iterations = (nextFrame - meshSkinned.GetLayerFrame(fullBodyLayerNum)) / totalFrames;
                        int bounces = (int)MathLib.Round(MathLib.Negative(iterations), MidpointRounding.ToZero);
                        if (bounces % 2 != 0)
                        {
                            //odd number of additional bounces
                            return meshSkinned.GetLayerNumFrames(fullBodyLayerNum) - (MathLib.Negative(nextFrame - meshSkinned.GetLayerFrame(fullBodyLayerNum)) % totalFrames);
                        }
                        else
                        {
                            //even number of additional bounces
                            fullBodyPingPongForward = true;
                            return MathLib.Negative(nextFrame - meshSkinned.GetLayerFrame(fullBodyLayerNum)) % totalFrames;
                        }

                    }
                }
                else
                    return nextFrame;
            }
        }
    }

    private int GetTotalFullBodyFrames(NamedAnimation fullBodyAnimation, int layerNum)
    {
        if (fullBodyAnimation.from == -1 && fullBodyAnimation.to == -1)
            return meshSkinned.GetLayerNumFrames(layerNum);
        int startFrame = fullBodyAnimation.from == -1 ? 0 : fullBodyAnimation.from;
        int endFrame = fullBodyAnimation.to == -1 ? meshSkinned.GetLayerNumFrames(layerNum) : fullBodyAnimation.to - 1;
        return endFrame - startFrame;
    }

    private void UpdateFullBodyBlend(Boolean fullBodyBlending, Boolean layeredFullBodyBlending)
    {
        float lerpAmount;//choose which blend speed factor to use
        //float delta = updateTimeDelta;
        float blendTarget;
        if (!layeredFullBodyBlending)
        {
            blendTarget = fullBodyBlendTarget;
            lerpAmount = fullBodyBlendSpeedFactor * updateTimeDelta * timeScale;
        }
        else
        {
            if (!fullBodyBlending)
            {
                blendTarget = layeredFullBodyBlendTarget;
                lerpAmount = layeredFullBodyBlendSpeedFactor * updateTimeDelta * timeScale;
            }
            else
            {   //edge case: both base and layered are blending
                //take the higher of the blend targets 
                blendTarget = fullBodyBlendTarget > layeredFullBodyBlendTarget ? fullBodyBlendTarget : layeredFullBodyBlendTarget;
                //we're changing blend amount and have both a base and layered animation present
                //fully blend in when either of them is fully blended in, but dont blend out until theyre both blended out
                //take the higher of the speed factors if blending in and the lower of the speed factors if blending out
                float speedFactor;
                if (blendTarget == 1.0f)
                    speedFactor = fullBodyBlendSpeedFactor > layeredFullBodyBlendSpeedFactor ? fullBodyBlendSpeedFactor : layeredFullBodyBlendSpeedFactor;
                else
                    speedFactor = fullBodyBlendSpeedFactor < layeredFullBodyBlendSpeedFactor ? fullBodyBlendSpeedFactor : layeredFullBodyBlendSpeedFactor;
                lerpAmount = speedFactor * updateTimeDelta * timeScale;
            }
        }
        if (blendTarget > fullBodyBlendAmount)
        {
            fullBodyBlendAmount = (float)Unigine.MathLib.Min(fullBodyBlendAmount + lerpAmount, 1.0f);
            if (fullBodyBlendAmount == 1.0f && layeredFullBodyBlendTarget != 1.0f)
            {
                onFullBodyBlendedIn.Run(fullBodyAnimationName);
            }
        }

        else if (blendTarget < fullBodyBlendAmount)
        {
            fullBodyBlendAmount = (float)Unigine.MathLib.Max(fullBodyBlendAmount - lerpAmount, 0.0f);
            if (fullBodyBlendAmount == 0.0f)
            {
                //reset triggers once base animation is blended out
                //updateLayeredFullBodyBlend will call this for the layered animation
                if (layeredFullBodyAnimationName == null && fullBodyAnimationName != null)
                {
                    ResetTriggersForAnimation(fullBodyAnimationName, fbAnimationTriggers);

                    meshSkinned.SetLayerFrame(fullBodyLayerNum, 0.0f);
                    fullBodyAnimationName = null;
                    onFullBodyBlendedOut.Run(fullBodyAnimationName);
                }
            }
        }
    }

    private void UpdatePrevCurFullBodyBlend()
    {
        float lerpAmount = fullBodyBlendSpeedFactor * updateTimeDelta * timeScale;//full body transition blend speed
        prevCurFullBodyBlendAmount = (float)Unigine.MathLib.Min(prevCurFullBodyBlendAmount + lerpAmount, 1.0f);
    }

    private void UpdatePrevCurLayeredFullBodyBlend()
    {
        float lerpAmount = layeredFullBodyBlendSpeedFactor * updateTimeDelta * timeScale;//layered full body transition blend speed
        prevCurLayeredFullBodyBlendAmount = (float)Unigine.MathLib.Min(prevCurLayeredFullBodyBlendAmount + lerpAmount, 1.0f);
    }

    private void UpdateLayeredFullBodyBlend()
    {
        float lerpAmount = layeredFullBodyBlendSpeedFactor * updateTimeDelta * timeScale;//layered full body blend in speed

        if (layeredFullBodyBlendTarget > layeredFullBodyBlendAmount)
        {
            layeredFullBodyBlendAmount = (float)Unigine.MathLib.Min(layeredFullBodyBlendAmount + lerpAmount, 1.0f);
            if (layeredFullBodyBlendAmount == 1.0f)
            {
                onLayeredFullBodyBlendedIn.Run(layeredFullBodyAnimationName);
            }
        }
        else if (layeredFullBodyBlendTarget < layeredFullBodyBlendAmount)
        {
            layeredFullBodyBlendAmount = (float)Unigine.MathLib.Max(layeredFullBodyBlendAmount - lerpAmount, 0.0f);
            if (layeredFullBodyBlendAmount == 0.0f)
            {
                //reset triggers once blended out
                ResetTriggersForAnimation(layeredFullBodyAnimationName, fbAnimationTriggers);

                meshSkinned.SetLayerFrame(layeredFullBodyLayerNum, 0);
                layeredFullBodyAnimationName = null;
                onLayeredFullBodyBlendedOut.Run(fullBodyAnimationName);
            }
        }
    }

