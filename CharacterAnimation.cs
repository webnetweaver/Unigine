    public void SetScopeAnimation(string scopeName, string animationName, float speedFactor = 3.75f)
    {
        //aleways reset trigger for the current fullbody animation
        //regardless of whether we're switching animations or not
        foreach (Scope scope in scopes)
        {
            if (scope.name == scopeName)
            {
                if (scope.baseAnimationName != null && (scopeAnimationTriggers.Count > 0))
                {
                    ResetTriggersForAnimation(scope.baseAnimationName, scopeAnimationTriggers);
                }

                if (scope.baseAnimationName == animationName)
                {
                    //resetting the same animation
                    //copy bone states to prev layer, reset prevCur blend and reset current frame
                    //fullBodyBlendAmount = 0.0f;
                    meshSkinned.CopyLayer(scope.prevLayerNum, 0);
                    scope.prevCurBlendAmount = 0.0f;
                    NamedAnimation animation = GetNamedAnimation(animationName, scope.animations);
                    if (animation.frameNumber == -1)
                        meshSkinned.SetLayerFrame(scope.layerNumber, 0);
                    scope.blendSpeedFactor = speedFactor;
                    return;
                }
                foreach (NamedAnimation animation in scope.animations)
                {
                    if (animation.name == animationName)
                    {
                        if (animation.asset.IsNull || !animation.asset.IsFileExist)
                        {
                            Log.Message($"\nSetScopeAnimation: No animation data found for scope animation name {animationName}\n");
                            return;
                        }

                        if (scope.baseAnimationName != null)
                        {
                            //switching animations
                            //initialize prevCurr blend value and swap base layer and prev layer numbers
                            var tempLayerNum = scope.prevLayerNum;
                            scope.prevLayerNum = scope.layerNumber;
                            scope.layerNumber = tempLayerNum;
                            scope.previousBaseAnimationName = scope.baseAnimationName;
                            scope.prevCurBlendAmount = 0.0f;
                            scope.framesPerSecond = animation.framesPerSecond;
                        }
                        else
                        {
                            //setting a new animation
                            //copy current bone states to prev layer, reset prevCur blend and reset current frame
                            scope.blendAmount = 0.0f;
                            scope.blendTarget = 1.0f;
                            meshSkinned.CopyLayer(scope.prevLayerNum, 0);
                            scope.prevCurBlendAmount = 0.0f;
                            scope.framesPerSecond = animation.framesPerSecond;
                        }
                        //requested animation found
                        //load new animation into layer, reset frame
                        scope.baseAnimationName = animationName;
                        meshSkinned.SetLayerAnimationFilePath(scope.layerNumber, animation.asset.Path);
                        if (animation.frameNumber == -1)
                            meshSkinned.SetLayerFrame(scope.layerNumber, 0);
                        else
                            meshSkinned.SetLayerFrame(scope.layerNumber, animation.frameNumber);
                        scope.blendSpeedFactor = speedFactor;
                        return;
                    }
                }
                Log.Warning($"\nSetScopeAnimation: animation {animationName} not found in scope {scopeName}");
                break;
            }
        }
    }

