    public mat4 GetBoneLocalTransform(string boneName)
    {
        int index = meshSkinned.FindBone(boneName);
        if (index == -1)
        {
            Log.Warning($"getBoneTransform: bone name {boneName} not found");
            return mat4.ZERO;
        }
        return meshSkinned.GetLayerBoneTransform(0, index);
    }
    public Mat4 GetBoneWorldTransform(string boneName)
    {
        int index = meshSkinned.FindBone(boneName);
        if (index == -1)
        {
            Log.Warning($"getBoneTransform: bone name {boneName} not found");
            return mat4.ZERO;
        }
        return meshSkinned.WorldTransform * meshSkinned.GetLayerBoneTransform(0, index);
    }

    public quat GetBoneLocalRotation(string boneName)
    {
        int index = meshSkinned.FindBone(boneName);
        if (index == -1)
        {
            Log.Warning($"getBoneTransform: bone name {boneName} not found");
            return quat.IDENTITY;
        }
        return meshSkinned.GetLayerBoneRotation(0, index);
    }
    public quat GetBoneWorldRotation(string boneName)
    {
        int index = meshSkinned.FindBone(boneName);
        if (index == -1)
        {
            Log.Warning($"getBoneTransform: bone name {boneName} not found");
            return quat.IDENTITY;
        }
        return meshSkinned.GetRotation() * meshSkinned.GetLayerBoneRotation(0, index);
    }
