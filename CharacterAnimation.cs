    private void UpdateBoneTransforms()
    {

        float delta = updateTimeDelta * timeScale;
        for (int i = boneInterpolatedRotations.Count - 1; i >= 0; i--)
        {
            BoneInterpolatedRotation boneInterpolatedRotation = boneInterpolatedRotations[i];
            if (boneInterpolatedRotation.blendAmount == 1.0f && boneInterpolatedRotation.blendTarget == 1.0f)
            {
                ApplyBoneRotation(boneInterpolatedRotation.boneIndex, boneInterpolatedRotation.currentRotation);
                continue;
            }
            else
            {
                //Log.Warning($"\nblendamount: {boneInterpolatedRotation.blendAmount}\n");
                if (boneInterpolatedRotation.blendAmount < 1.0f && boneInterpolatedRotation.blendTarget == 1.0f)
                {
                    //updating interpolate blend
                    //blending from currentRotation to targetRotation
                    float deltaBlend = (boneInterpolatedRotation.degreesPerSecond * delta) / boneInterpolatedRotation.angularDistance;
                    boneInterpolatedRotation.blendAmount = (float)Unigine.MathLib.Min(boneInterpolatedRotation.blendAmount + deltaBlend, 1.0f);
                    ApplyBoneInterpolatedRotation(boneInterpolatedRotation);
                    if (boneInterpolatedRotation.blendAmount == 1.0f)
                    {
                        onBoneInterpolateDone.Run(boneInterpolatedRotation.boneName);
                    }
                }
                else if (boneInterpolatedRotation.blendAmount > 0.0f && boneInterpolatedRotation.blendTarget == 0.0f)
                {
                    //removing interpolate
                    //blending from currentRotation to transform to layer rotation

                    float deltaBlend = (boneInterpolatedRotation.degreesPerSecond * delta) / boneInterpolatedRotation.angularDistance;
                    boneInterpolatedRotation.blendAmount = (float)Unigine.MathLib.Max(boneInterpolatedRotation.blendAmount - deltaBlend, 0.0f);
                    ApplyBoneInterpolatedRotation(boneInterpolatedRotation);
                    if (boneInterpolatedRotation.blendAmount == 0.0f)
                    {
                        boneInterpolatedRotations.Remove(boneInterpolatedRotation);
                        onClearBoneInterpolateDone.Run(boneInterpolatedRotation.boneName);
                    }
                }

            }
        }

        for (int i = boneRotations.Count - 1; i >= 0; i--)
        {
            BoneRotation boneRotation = boneRotations[i];
            if (boneRotation.blendOutDegreesPerSecond == 0.0f)
            {
                ApplyBoneRotation(boneRotation.boneIndex, boneRotation.rotation);
            }
            else
            {
                UpdateRotationBlendOut(boneRotation.boneIndex);
                ApplyBoneRotation(boneRotation.boneIndex, boneRotation.rotation);
                if (boneRotation.blendOutAmount == 1.0f)
                {
                    boneRotations.Remove(boneRotation);
                    onClearBoneRotationSmoothDone.Run(meshSkinned.GetBoneName(boneRotation.boneIndex));
                }
            }
        }
    }

    private void ApplyBoneInterpolatedRotation(BoneInterpolatedRotation boneInterpolatedRotation)
    {
        //mat4 currentXform = meshSkinned.GetLayerBoneTransform(0, boneInterpolatedRotation.boneIndex);
        quat currentRotation;
        //Log.Warning("\ngetting here "+ boneInterpolatedRotation.blendAmount);
        //quat remainingQuat;
        if (boneInterpolatedRotation.blendTarget == 1.0f)
        {
            currentRotation = Unigine.MathLib.Slerp(boneInterpolatedRotation.startRotation, boneInterpolatedRotation.targetRotation, boneInterpolatedRotation.blendAmount);
            //remainingQuat = boneInterpolatedRotation.currentRotation - boneInterpolatedRotation.targetRotation;
        }
        else
        {
            currentRotation = Unigine.MathLib.Slerp(quat.IDENTITY, boneInterpolatedRotation.targetRotation, boneInterpolatedRotation.blendAmount);
            //remainingQuat = boneInterpolatedRotation.currentRotation - meshSkinned.GetLayerBoneRotation(0, boneInterpolatedRotation.boneIndex);
        }
        //remainingQuat.Get(out vec3 axis, out float angle);
        //boneInterpolatedRotation.angularDistance = angle;
        boneInterpolatedRotation.currentRotation = currentRotation;
        meshSkinned.SetLayerBoneRotation(0, boneInterpolatedRotation.boneIndex, meshSkinned.GetLayerBoneRotation(0, boneInterpolatedRotation.boneIndex) * currentRotation);
    }

