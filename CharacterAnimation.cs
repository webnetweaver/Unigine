    public void SetProcessRate(int fps)
    {
        if (fps > 0)
        {
            processRate = fps;
            processType = AnimationProcessTypes.CustomRate;
            updateTime = (float)(1.0f / processRate);
        }
        else
        {
            processType = AnimationProcessTypes.EveryFrame;
            Log.Warning($"setBlendTreeUpdate: fps must be greater than 0");
        }
    }

    public int GetProcessRate()
    {
        return processRate;
    }

