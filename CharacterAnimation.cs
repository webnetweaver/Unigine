                    meshSkinned.AddLayer();
                    poseBsAnimationList.layerIndex = meshSkinned.NumLayers - 1;
                }

                //meshSkinned.SetLayerAnimationFilePath(2, GetVirtualPath("/model/Waving.fbx/Waving.anim"));
                meshSkinned.SetLayerAnimationFilePath(meshSkinned.NumLayers - 1, poseBlendSpaceAnimation.asset.Path);

                if (poseBlendSpaceAnimation.frameNumber > -1)
                {
                    if (poseBlendSpaceAnimation.frameNumber <= meshSkinned.GetLayerNumFrames(meshSkinned.NumLayers - 1))
                    {
                        meshSkinned.SetLayerFrame(meshSkinned.NumLayers - 1, poseBlendSpaceAnimation.frameNumber);
                    }
                    else
                    {
                        Log.Warning($"frame number: {poseBlendSpaceAnimation.frameNumber} larger than maximum animation frame for {poseBlendSpaceAnimation.name} pose blend space animation");
                    }
                }

                // add point and weight for blending
                poseBsAnimationList.points.Add(poseBlendSpaceAnimation.point);
                poseBsAnimationList.weights.Add(1.0f);
                //Log.Warning(poseBsAnimationList.points[i]);
            }

            if (poseBsAnimationList.AllBones)
            {
                poseBsAnimationList.boneIndices.Add(0);
                poseBsAnimationList.boneIndices.AddRange(GetAllBoneChildren(0));
            }
            else
            {
                if (poseBsAnimationList.rootBones.Count > 0)
                {
                    foreach (string rootBoneName in poseBsAnimationList.rootBones)
                    {
                        int rootBoneIndex = meshSkinned.FindBone(rootBoneName);
                        if (rootBoneIndex > -1)
                        {
                            poseBsAnimationList.boneIndices.Add(rootBoneIndex);
                            poseBsAnimationList.boneIndices.AddRange(GetAllBoneChildren(rootBoneIndex));
                        }
                        else
                        {
                            Log.Warning($"root bone {rootBoneName} not found for pose blend space: {poseBsAnimationList.name}");
                        }
                    }
                }
                if (poseBsAnimationList.bones.Count > 0)
                {
                    foreach (string boneName in poseBsAnimationList.bones)
                    {
                        int boneIndex = meshSkinned.FindBone(boneName);
                        if (boneIndex > -1)
                        {
                            poseBsAnimationList.boneIndices.Add(boneIndex);
                        }
                        else
                        {
                            Log.Warning($"bone {boneName} not found for pose blend space: {poseBsAnimationList.name}");
                        }
                    }
                }
            }
            poseBsAnimationList.boneIndices = [.. poseBsAnimationList.boneIndices.Distinct()];

            if (poseBsAnimationList.boneIndices.Count == 0)
                Log.Warning($"No selected bones for pose blend space {poseBsAnimationList.name}");

        }
    }
    private void UpdatePoseBlendSpaces()
    {
        float delta = updateTimeDelta * timeScale;

        // update weight for each layer
        UpdatePoseBsWeights();
        //variables specific to blendspace list
        //foreach (string poseBlendSpaceName in activePoseBlendSpaceNames)
        for (int i = activePoseBlendSpaceNames.Count - 1; i >= 0; i--)
        {
            //iterate through updates in reverse order to allow removals from the list
            string poseBlendSpaceName = activePoseBlendSpaceNames[i];
            PoseBlendSpaceAnimationList bsAnimList = GetPoseBlendSpaceListByName(poseBlendSpaceName);
            int blendSpaceLayerIndex = bsAnimList.layerIndex;
            List<PoseBlendSpaceAnimation> blendSpaceAnimations = bsAnimList.animations;
            //List<float> defaultFrequencies = bsAnimList.defaultFrequencies;
            List<float> weights = bsAnimList.weights;
            int bsHighestWeightIndex = bsAnimList.bsHighestWeightIndex;
            bool blendSpaceBlending = !(bsAnimList.blendTarget == 1.0 && bsAnimList.blendAmount == 1.0);
            bool blendSpacePrevCurBlending = !(bsAnimList.prevCurBlendAmount == 1.0);
            /*bool scopeBlending = !(scope.blendTarget == 1.0 && scope.blendAmount == 1.0);
            bool scopeLayerBlending = !(scope.layeredBlendTarget == 1.0 && scope.layeredBlendAmount == 1.0);
            bool scopePrevCurBlending = !(scope.prevCurBlendAmount == 1.0);*/
            if (!blendSpacePrevCurBlending)
            {
                for (int ii = blendSpaceLayerIndex; ii < (blendSpaceLayerIndex + blendSpaceAnimations.Count); ii++)
                {
                    if (bsAnimList.animations[ii - blendSpaceLayerIndex].frameNumber == -1)
                    {
                        float frame = meshSkinned.GetLayerFrame(ii) + bsAnimList.animations[ii - blendSpaceLayerIndex].framesPerSecond * delta;
                        meshSkinned.SetLayerFrame(ii, frame, bsAnimList.animations[ii - blendSpaceLayerIndex].from, bsAnimList.animations[ii - blendSpaceLayerIndex].to);
                    }
                    else
                    {
                        meshSkinned.SetLayerFrame(ii, bsAnimList.animations[ii - blendSpaceLayerIndex].frameNumber);
                    }
                    if ((blendSpaceLayerIndex != ii) && weights[ii - blendSpaceLayerIndex] > MathLib.EPSILON)
                    {
                        meshSkinned.LerpLayer(blendSpaceLayerIndex, blendSpaceLayerIndex, ii, weights[ii - blendSpaceLayerIndex]);
                    }
                }
            }
            else
            {
                //pose blend space is blending in, update the prevCur blend amount, place new state in blendSpaceLayerIndex, interpolate between prev - blendSpaceLayerIndex based on blend amt
                UpdatePrevCurPoseBlend(bsAnimList);
                for (int ii = blendSpaceLayerIndex; ii < (blendSpaceLayerIndex + blendSpaceAnimations.Count); ii++)
                {
                    if (bsAnimList.animations[ii - blendSpaceLayerIndex].frameNumber == -1)
                    {
                        float frame = meshSkinned.GetLayerFrame(ii) + bsAnimList.animations[ii - blendSpaceLayerIndex].framesPerSecond * delta;
                        meshSkinned.SetLayerFrame(ii, frame);
                    }
                    else
                    {
                        meshSkinned.SetLayerFrame(ii, bsAnimList.animations[ii - blendSpaceLayerIndex].frameNumber);
                    }
                    if ((blendSpaceLayerIndex != ii) && weights[ii - blendSpaceLayerIndex] > MathLib.EPSILON)
                    {
                        meshSkinned.LerpLayer(blendSpaceLayerIndex, blendSpaceLayerIndex, ii, weights[ii - blendSpaceLayerIndex]);
                    }
                }
                if (bsAnimList.prevCurBlendAmount < 1.0f)
                    meshSkinned.LerpLayer(blendSpaceLayerIndex, bsAnimList.prevLayerIndex, blendSpaceLayerIndex, bsAnimList.prevCurBlendAmount);
                else
                {
                    meshSkinned.ClearLayer(bsAnimList.prevLayerIndex);
                    if (!blendSpaceBlending)
                        onPoseBlendSpaceSetTargetPointBlendedComplete.Run(bsAnimList.name);
                }

            }
            if (blendSpaceBlending)
            {
                UpdatePoseBlendSpaceBlend(bsAnimList);
                if (bsAnimList.blendAmount == 1.0f)
                {
                    if (bsAnimList.swappedName != null)
                    {
                        onPoseBlendSpaceSwapComplete.Run(poseBlendSpaceName, bsAnimList.swappedName);
                        bsAnimList.swappedName = null;
                    }
                }
                else if (bsAnimList.blendAmount == 0.0f)
                {
                    RemovePoseBlendSpaceImmediate(poseBlendSpaceName);
                    continue;
                }
            }

            //applyPose(bsAnimList);
            foreach (AnimationTrigger animationTrigger in poseBsAnimationTriggers)
            {
                if (animationTrigger.animation == blendSpaceAnimations[bsHighestWeightIndex].name)
                {
                    int layerNum = blendSpaceLayerIndex + bsHighestWeightIndex;
                    int totalFrames = GetTotalPoseFrames(poseBlendSpaceName, animationTrigger.animation);
                    CheckTrigger(animationTrigger, layerNum, totalFrames);
                }
                else
                {
                    animationTrigger.currentFrame = -1.0f;
                }
            }

            if (bsAnimList.immediatePoint.x == -2)
            {
                if (bsAnimList.movement.Length2 > MathLib.EPSILON)
                {
                    vec2 movementDelta = bsAnimList.movement * delta * bsAnimList.speed;
                    bsAnimList.currentPoint += movementDelta;

                    if (bsAnimList.targetPoint.x != -2)
                    {
                        if ((bsAnimList.targetPoint - bsAnimList.currentPoint).Length2 < movementDelta.Length2 * 2)
                        {
                            bsAnimList.currentPoint = bsAnimList.targetPoint;
                            bsAnimList.movement = vec2.ZERO;
                            bsAnimList.targetPoint.x = -2;
                            onPoseBlendSpaceSetTargetPointComplete.Run(bsAnimList.name);
                        }
                    }
                }
            }
            else
                bsAnimList.currentPoint = bsAnimList.immediatePoint;

            bsAnimList.currentPoint = MathLib.Clamp(bsAnimList.currentPoint, new vec2(-1, -1), new vec2(1, 1));
        }
        foreach (string poseBlendSpaceName in activePoseBlendSpaceNames)
        {
            //apply the poses in the order they appear in the list
            PoseBlendSpaceAnimationList bsAnimList = GetPoseBlendSpaceListByName(poseBlendSpaceName);
            ApplyPose(bsAnimList);
        }
    }

    private void UpdatePrevCurPoseBlend(PoseBlendSpaceAnimationList poseBsAnimList)
    {
