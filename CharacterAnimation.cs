        if (fullBodyAnimationName != null && (fbAnimationTriggers.Count > 0))
        {
            ResetTriggersForAnimation(fullBodyAnimationName, fbAnimationTriggers);
        }

        if (fullBodyAnimationName == animationName)
        {
            //resetting the same animation
            //copy bone states to prev layer, reset prevCur blend and reset current frame
            //fullBodyBlendAmount = 0.0f;
            meshSkinned.CopyLayer(prevFullBodyLayerNum, 0);
            prevCurFullBodyBlendAmount = 0.0f;
            NamedAnimation animation = GetNamedAnimation(animationName, FullBodyAnimations);
            if (animation.frameNumber == -1)
                meshSkinned.SetLayerFrame(fullBodyLayerNum, 0);
            fullBodyBlendSpeedFactor = speedFactor;
            return;
        }
        foreach (NamedAnimation animation in FullBodyAnimations)
        {
            if (animation.name == animationName)
            {
                if (animation.asset.IsNull || !animation.asset.IsFileExist)
                {
                    Log.Message($"\nSetFullBodyAnimation: No animation data found for animation name {animationName}\n");
                    return;
                }

                if (fullBodyAnimationName != null)
                {
                    //switching animations
                    //initialize prevCurr blend value and swap fullbody and prev layer numbers
                    var tempLayerNum = prevFullBodyLayerNum;
                    prevFullBodyLayerNum = fullBodyLayerNum;
                    fullBodyLayerNum = tempLayerNum;
                    previousfullBodyAnimationName = fullBodyAnimationName;
                    prevCurFullBodyBlendAmount = 0.0f;
                    fullBodyFPS = animation.framesPerSecond;
                }
                else
                {
                    //setting a new animation
                    //copy current bone states to prev layer, reset prevCur blend and reset current frame
                    fullBodyBlendAmount = 0.0f;
                    fullBodyBlendTarget = 1.0f;
                    meshSkinned.CopyLayer(prevFullBodyLayerNum, 0);
                    prevCurFullBodyBlendAmount = 0.0f;
                    fullBodyFPS = animation.framesPerSecond;
                }

                //requested animation found
                //load new animation into layer, reset frame
                fullBodyAnimationName = animationName;
                meshSkinned.SetLayerAnimationFilePath(fullBodyLayerNum, animation.asset.Path);
                if (animation.frameNumber == -1)
                    meshSkinned.SetLayerFrame(fullBodyLayerNum, 0);
                else
                    meshSkinned.SetLayerFrame(fullBodyLayerNum, animation.frameNumber);
                fullBodyBlendSpeedFactor = speedFactor;
                return;
            }
        }
        Log.Warning($"\nSetFullBodyAnimation: animation name {animationName} not found");
    }

