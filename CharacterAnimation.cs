    private void UpdatePoseBlendSpaceBlend(PoseBlendSpaceAnimationList poseBsAnimList)
    {
        float lerpAmount = poseBsAnimList.blendSpeedFactor * updateTimeDelta * timeScale;//pose blend in speed
        //float delta = updateTimeDelta;
        float blendTarget = poseBsAnimList.blendTarget;

        if (blendTarget > poseBsAnimList.blendAmount)
        {
            poseBsAnimList.blendAmount = (float)Unigine.MathLib.Min(poseBsAnimList.blendAmount + lerpAmount, 1.0f);
            if (poseBsAnimList.blendAmount == 1.0f)
                onPoseBlendSpaceBlendedIn.Run(poseBsAnimList.name);
        }
        else if (blendTarget < poseBsAnimList.blendAmount)
        {
            poseBsAnimList.blendAmount = (float)Unigine.MathLib.Max(poseBsAnimList.blendAmount - lerpAmount, 0.0f);
            if (poseBsAnimList.blendAmount == 0.0f)
                onPoseBlendSpaceBlendedOut.Run(poseBsAnimList.name);
        }
    }
