    public void SwapPoseBlendSpaces(string oldPoseBlendSpaceName, string newPoseBlendSpaceName, float speedFactor = 3.75f)
    {
        PoseBlendSpaceAnimationList oldPoseBsAnimList = GetPoseBlendSpaceListByName(oldPoseBlendSpaceName);
        if (oldPoseBsAnimList == null)
        {
            Log.Warning($"SwapPoseBlendSpaces(): {oldPoseBlendSpaceName} pose blend space not found");
            return;
        }
        PoseBlendSpaceAnimationList newPoseBsAnimList = GetPoseBlendSpaceListByName(newPoseBlendSpaceName);
        if (newPoseBsAnimList == null)
        {
            Log.Warning($"SwapPoseBlendSpaces(): {newPoseBlendSpaceName} pose blend space not found");
            return;
        }
        if (oldPoseBsAnimList.blendTarget == 0.0f && newPoseBsAnimList.blendTarget == 1.0f && newPoseBsAnimList.swappedName == oldPoseBlendSpaceName)
        {
            //swap already in progress
            return;
        }
        if (oldPoseBsAnimList.blendTarget == 0.0f)
        {
            Log.Warning($"SwapPoseBlendSpaces(): attempt to switch from {oldPoseBlendSpaceName} which is not active or already being swapped");
            return;
        }
        if (newPoseBsAnimList.blendTarget == 1.0f)
        {
            Log.Warning($"SwapPoseBlendSpaces(): attempt to switch to {newPoseBlendSpaceName} which is already active or being swapped to");
            return;
        }

        if (activePoseBlendSpaceNames.Contains(newPoseBlendSpaceName))
        {
            //blending to a previously active pose before it was completely blended out
            //remove the new name from the current list and AddPoseBlendSpaceSwap() will reinsert it after oldPoseBlendSpaceName
            activePoseBlendSpaceNames.Remove(newPoseBlendSpaceName);

        }

        RemovePoseBlendSpace(oldPoseBlendSpaceName);
        AddPoseBlendSpaceSwap(newPoseBlendSpaceName, oldPoseBlendSpaceName);
        newPoseBsAnimList.blendSpeedFactor = speedFactor;
