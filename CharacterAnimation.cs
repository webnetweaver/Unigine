    private void InitScopes()
    {
        foreach (var scope in scopes)
        {
            if (scope.rootBones.Count > 0)
            {
                foreach (string rootBoneName in scope.rootBones)
                {
                    int rootBoneIndex = meshSkinned.FindBone(rootBoneName);
                    if (rootBoneIndex > -1)
                    {
                        scope.boneIndices.Add((int)rootBoneIndex);
                        scope.boneIndices.AddRange(GetAllBoneChildren(rootBoneIndex));
                    }
                    else
                    {
                        Log.Warning($"root bone {rootBoneName} not found for scope: {scope.name}");
                    }
                }
            }
            if (scope.bones.Count > 0)
            {
                foreach (string boneName in scope.bones)
                {
                    int boneIndex = meshSkinned.FindBone(boneName);
                    if (boneIndex > -1)
                    {
                        scope.boneIndices.Add((int)boneIndex);
                    }
                    else
                    {
                        Log.Warning($"root bone {boneName} not found for scope: {scope.name}");
                    }
                }
            }

            scope.boneIndices = [.. scope.boneIndices.Distinct()];

            meshSkinned.AddLayer();
            scope.layerNumber = meshSkinned.NumLayers - 1;
            meshSkinned.AddLayer();
            scope.prevLayerNum = meshSkinned.NumLayers - 1;
            meshSkinned.AddLayer();
            scope.layeredScopeLayerNum = meshSkinned.NumLayers - 1;
            meshSkinned.AddLayer();
            scope.prevLayeredScopeLayerNum = meshSkinned.NumLayers - 1;

            if (scope.boneIndices.Count == 0)
                Log.Warning($"No selected bones for scope {scope.name}");

        }
    }

    private void UpdateScopes()
    {
        foreach (Scope scope in scopes)
        {
            bool scopePresent = scope.baseAnimationName != null;
            bool scopeLayerPresent = scope.layeredAnimationName != null;
            if (scopePresent || scopeLayerPresent)
            {
                bool scopeBlending = !(scope.blendTarget == 1.0 && scope.blendAmount == 1.0);
                bool scopeLayerBlending = !(scope.layeredBlendTarget == 1.0 && scope.layeredBlendAmount == 1.0);
                bool scopePrevCurBlending = !(scope.prevCurBlendAmount == 1.0);
                bool scopeLayeredPrevCurBlending = !(scope.prevCurLayeredBlendAmount == 1.0);
                AdvanceScopeFrame(scope);//meshSkinned.SetLayerFrame() updated advance to update base and oneshot and start blending out when done
                if (scopePrevCurBlending)
                {
                    UpdatePrevCurScopeBlend(scope);
                    if (scope.prevCurBlendAmount < 1.0f)
                        ApplyScopePrevCur(scope);//LerpLayer between prev and curr FullBody layer into fullBodyLayer
                    else
                    {
                        meshSkinned.ClearLayer(scope.prevLayerNum);
                        onScopeAnimationChangeComplete.Run(scope.baseAnimationName, scope.previousBaseAnimationName);
                    }
                }

                if (scopeLayerPresent)
                {
                    if (scopeLayeredPrevCurBlending)
                    {
                        UpdatePrevCurLayeredScopeBlend(scope);
                        if (scope.prevCurLayeredBlendAmount < 1.0f)
                            ApplyLayeredScopePrevCur(scope);//LerpLayer between prev and curr layered FullBody layer into layeredFullBodyLayer
                        else
                        {
                            meshSkinned.ClearLayer(scope.prevLayeredScopeLayerNum);
                            onLayeredScopeAnimationChangeComplete.Run(scope.layeredAnimationName, scope.previousLayeredAnimationName);
                        }
                    }
                    if (scopeLayerBlending)
                        UpdateLayeredScopeBlend(scope);//update layeredFullBodyBlendAmount potentially meshSkinned.SetLayerFrame(0) if oneshot is blended completely out//oneshot blend is updating

                    ApplyScopeLayered(scope);//LerpLayer layeredFullBody layer into fullBodyLayer
                }

                if (scopeBlending || (scopeLayerPresent && scopeLayerBlending))
                {
                    UpdateScopeBlend(scope, scopeBlending, (scopeLayerPresent && scopeLayerBlending));//update fullBodyBlendAmount potentially meshSkinned.SetLayerFrame(0) if base animation is blended completely out
                }
                ApplyScope(scope);

                //check animation triggers
                if (scopeAnimationTriggers.Count > 0)
                {
                    string animationToCheck;
                    int layerToCheck;
                    if (scope.layeredAnimationName != null)
                    {
                        animationToCheck = scope.layeredAnimationName;
                        layerToCheck = scope.layeredScopeLayerNum;
                    }
                    else
                    {
                        NamedAnimation scopeAnimation = GetNamedAnimation(scope.baseAnimationName, scope.animations);
                        if (scopeAnimation.frameNumber != -1)
                        {
                            return;
                        }
                        animationToCheck = scope.baseAnimationName;
                        layerToCheck = scope.layerNumber;
                    }

                    foreach (AnimationTrigger animationTrigger in scopeAnimationTriggers)
                    {
                        if (animationTrigger.animation == animationToCheck)
                        {
                            NamedAnimation scopeAnimation = GetNamedAnimation(animationToCheck, scope.animations);
                            int totalFrames = GetTotalScopeFrames(scopeAnimation, layerToCheck);
                            CheckTrigger(animationTrigger, layerToCheck, totalFrames, !scope.pingPongForward);
                        }
                    }
                }
            }
        }
    }

    private void UpdatePrevCurScopeBlend(Scope scope)
    {
        float lerpAmount = scope.blendSpeedFactor * updateTimeDelta * timeScale;//scop transition speed
        scope.prevCurBlendAmount = (float)Unigine.MathLib.Min(scope.prevCurBlendAmount + lerpAmount, 1.0f);
    }

    private void UpdatePrevCurLayeredScopeBlend(Scope scope)
    {
        float lerpAmount = scope.layeredBlendSpeedFactor * updateTimeDelta * timeScale;//layered scope trnsition speed
        scope.prevCurLayeredBlendAmount = (float)Unigine.MathLib.Min(scope.prevCurLayeredBlendAmount + lerpAmount, 1.0f);
    }

    private void UpdateLayeredScopeBlend(Scope scope)
    {
        float lerpAmount = scope.layeredBlendSpeedFactor * updateTimeDelta * timeScale;//layered scope blend in speed

        if (scope.layeredBlendTarget > scope.layeredBlendAmount)
        {
            scope.layeredBlendAmount = (float)Unigine.MathLib.Min(scope.layeredBlendAmount + lerpAmount, 1.0f);
            if (scope.layeredBlendAmount == 1.0f)
            {
                onLayeredScopeBlendedIn.Run(scope.name);
            }
        }

        else if (scope.layeredBlendTarget < scope.layeredBlendAmount)
        {
            scope.layeredBlendAmount = (float)Unigine.MathLib.Max(scope.layeredBlendAmount - lerpAmount, 0.0f);
            if (scope.layeredBlendAmount == 0.0f)
            {
                //reset triggers once blended out
                ResetTriggersForAnimation(scope.layeredAnimationName, scopeAnimationTriggers);

                meshSkinned.SetLayerFrame(scope.layeredScopeLayerNum, 0);
                onLayeredScopeBlendedOut.Run(scope.name);
                scope.layeredAnimationName = null;
            }
        }
    }

    private void ApplyScopePrevCur(Scope scope)
    {
        meshSkinned.LerpLayer(scope.layerNumber, scope.prevLayerNum, scope.layerNumber, scope.prevCurBlendAmount);
    }

    private void ApplyLayeredScopePrevCur(Scope scope)
