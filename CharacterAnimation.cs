    private void InitBlendSpaces()
    {
        foreach (BlendSpaceAnimationList bsAnimationList in blendSpaces)
        {
            //meshSkinned.AddLayer();
            //bsAnimationList.layerIndex = meshSkinned.NumLayers - 1;

            List<BlendSpaceAnimation> blendSpaceAnimations = bsAnimationList.animations;
            //Log.Warning("\ncount: "+blendSpaceAnimations.Count+"\n");
            foreach (BlendSpaceAnimation blendSpaceAnimation in blendSpaceAnimations)
            {

                if (blendSpaceAnimation.asset.IsNull || !blendSpaceAnimation.asset.IsFileExist)
                {
                    Log.Message($"\nNo animation data found for blend space animation {blendSpaceAnimation.name}\n");
                    continue;
                }

                // create layer for each animation
                //int id = meshSkinned.AddAnimation(animations[i].asset.Path);//*
                meshSkinned.AddLayer();
                if (bsAnimationList.layerIndex == 0)//layer index and prevLayerIndex for the animation list hasnt been set yet
                {
                    bsAnimationList.prevLayerIndex = meshSkinned.NumLayers - 1;
                    meshSkinned.AddLayer();
                    bsAnimationList.layerIndex = meshSkinned.NumLayers - 1;
                }

                meshSkinned.SetLayerAnimationFilePath(meshSkinned.NumLayers - 1, blendSpaceAnimation.asset.Path);

                // enable new layer and set default weight to 1.0
                //meshSkinned.SetLayer(meshSkinned.NumLayers - 1, true, 1.0f);

                // calculate animation speed to play with fixed duration
                //int numFrames = meshSkinned.GetNumAnimationFrames(id);
                int numFrames = meshSkinned.GetLayerNumFrames(meshSkinned.NumLayers - 1);

                bsAnimationList.defaultFrequencies.Add(blendSpaceAnimation.framesPerSecond / numFrames);
                //animationLengths.Add(numFrames);
                if (blendSpaceAnimation.phaseShift)
                    meshSkinned.SetLayerFrame(meshSkinned.NumLayers - 1, numFrames / 2);

                // add point and weight for blending
                bsAnimationList.points.Add(blendSpaceAnimation.point);
                bsAnimationList.weights.Add(1.0f);
                //Log.Warning("\n" + bsAnimationList.points[i].x +", "+ bsAnimationList.points[i].y + "\n");
            }
        }

        if (blendSpaces.Count > 0)
        {
            currentBlendSpaceName = blendSpaces[0].name;
            currentBlendSpaceBlendAmount = 1.0f;
        }
    }
    private void UpdateBlendSpaces()
    {
        float delta = updateTimeDelta * timeScale;

        // update weight for each layer
        UpdateBsWeights();
        //variables specific to blendspace list
        BlendSpaceAnimationList bsAnimList = GetBlendSpaceListByName(currentBlendSpaceName);
        int blendSpaceLayerIndex = bsAnimList.layerIndex;
        List<BlendSpaceAnimation> blendSpaceAnimations = bsAnimList.animations;
        List<float> defaultFrequencies = bsAnimList.defaultFrequencies;
        List<float> weights = bsAnimList.weights;
        int bsHighestWeightIndex = bsAnimList.bsHighestWeightIndex;
        bool blendSpaceBlending = !(currentBlendSpaceBlendAmount == 1.0);

        if (!blendSpaceBlending)
        {
            for (int i = blendSpaceLayerIndex; i < (blendSpaceLayerIndex + blendSpaceAnimations.Count); i++)
            {
                float frame = meshSkinned.GetLayerFrame(i) + (weightedFreq / defaultFrequencies[i - blendSpaceLayerIndex]) * blendSpaceAnimations[i - blendSpaceLayerIndex].framesPerSecond * delta;
                meshSkinned.SetLayerFrame(i, frame);
                if (weights[i - blendSpaceLayerIndex] > MathLib.EPSILON)
                {
                    meshSkinned.LerpLayer(0, 0, i, weights[i - blendSpaceLayerIndex]);
                }
            }
        }
        else
        {
            //blend space is blending between 2 blendspaces, update the blend amount, place new state in layer o, interpolate between prev - zero based on blend amt
            UpdateBlendSpaceBlend();
            for (int i = blendSpaceLayerIndex; i < (blendSpaceLayerIndex + blendSpaceAnimations.Count); i++)
            {
                float frame = meshSkinned.GetLayerFrame(i) + (weightedFreq / defaultFrequencies[i - blendSpaceLayerIndex]) * blendSpaceAnimations[i - blendSpaceLayerIndex].framesPerSecond * delta;
                meshSkinned.SetLayerFrame(i, frame);
                if (weights[i - blendSpaceLayerIndex] > MathLib.EPSILON)
                {
                    meshSkinned.LerpLayer(0, 0, i, weights[i - blendSpaceLayerIndex]);
                }
            }
            if (currentBlendSpaceBlendAmount < 1.0f)
                meshSkinned.LerpLayer(0, bsAnimList.prevLayerIndex, 0, currentBlendSpaceBlendAmount);
            else
            {
                meshSkinned.ClearLayer(bsAnimList.prevLayerIndex);
                onBlendSpaceBlendedIn.Run(currentBlendSpaceName);
                if (previousBlendSpaceName != null)
                    onBlendSpaceBlendedOut.Run(previousBlendSpaceName);
            }

        }

        foreach (AnimationTrigger animationTrigger in bsAnimationTriggers)
        {
            if (animationTrigger.animation == blendSpaceAnimations[bsHighestWeightIndex].name)
            {
                int layerNum = blendSpaceLayerIndex + bsHighestWeightIndex;
                CheckTrigger(animationTrigger, layerNum, meshSkinned.GetLayerNumFrames(layerNum));
            }
            else
            {
                animationTrigger.currentFrame = -1.0f;
            }
        }

        if (movement.x != 0)
            targetPoint.x += movement.x * updateTimeDelta * 2.0f;
        else
            targetPoint.x *= MathLib.Exp(-updateTimeDelta * 3.0f);

        if (movement.y != 0)
            targetPoint.y += movement.y * updateTimeDelta * 2.0f;
        else
            targetPoint.y *= MathLib.Exp(-updateTimeDelta * 3.0f);

        targetPoint = MathLib.Clamp(targetPoint, new vec2(-1, -1), new vec2(1, 1));

    }
    private void UpdateBlendSpaceBlend()
    {
        float lerpAmount = blendSpaceBlendSpeedFactor * updateTimeDelta * timeScale;
        currentBlendSpaceBlendAmount = (float)Unigine.MathLib.Min(currentBlendSpaceBlendAmount + lerpAmount, 1.0f);
    }
    private BlendSpaceAnimationList GetBlendSpaceListByName(string name)
    {
        foreach (BlendSpaceAnimationList blendSpaceAnimationList in blendSpaces)
        {
            if (blendSpaceAnimationList.name == name)
                return blendSpaceAnimationList;
        }
        return null;
