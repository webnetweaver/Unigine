    public void QueueCommand(Action<CharacterAnimation> callback)//eg. QueueCommand((Blend2D blend2d) => blend2d.testLifeCycle()); or QueueCommand(testagain);
    {
        EventDelegate<CharacterAnimation> command = null;
        command = (characterAnimation) => {
            callback.Invoke(characterAnimation);
            postBlendTreeUpdate.Disconnect(command);
        };
        postBlendTreeUpdate.Connect(command);
    }
