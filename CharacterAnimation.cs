        for (int j = 0; j < numChildren; j++)
        {
            int boneChild = meshSkinned.GetBoneChild(rootBone, j);
            result.Add(boneChild);
            int numGrandChildren = meshSkinned.GetNumBoneChildren(boneChild);
            if (numGrandChildren > 0)
            {
                result.AddRange(GetBoneChildren(boneChild));
            }
        }
        return result;
    }

    private List<int> GetAllBoneChildren(int rootBone)
    {
        return GetBoneChildren(rootBone);
    }
