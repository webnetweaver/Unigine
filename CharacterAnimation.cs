    private void UpdatePoseBsWeights()
    {
        foreach (string poseBlendSpaceName in activePoseBlendSpaceNames)
        {
            PoseBlendSpaceAnimationList bsAnimationList = GetPoseBlendSpaceListByName(poseBlendSpaceName);
            if (bsAnimationList == null)
                return;
            // gradient band interpolation
            float totalWeight = 0.0f;
            float bsHighestWeight = -1;
            //weightedFreq = 0.0f;
            List<vec2> points = bsAnimationList.points;
            vec2 targetPoint = bsAnimationList.currentPoint;
            for (int i = 0; i < points.Count; i++)
            {
                vec2 pi = points[i];
                vec2 targetVector = targetPoint - pi;

                float weight = 1.0f;
                for (int j = 0; j < points.Count; j++)
                {
                    if (j == i)
                        continue;

                    vec2 jVector = points[j] - pi;

                    float newWeight = 1.0f - MathLib.Dot(targetVector, jVector) / jVector.Length2;
                    newWeight = MathLib.Clamp(newWeight, 0.0f, 1.0f);

                    weight = MathLib.Min(weight, newWeight);
                }

                bsAnimationList.weights[i] = weight;
                totalWeight += weight;
            }

            for (int i = 0; i < bsAnimationList.weights.Count; i++)
                bsAnimationList.weights[i] = bsAnimationList.weights[i] / totalWeight;

            for (int i = 0; i < bsAnimationList.weights.Count; i++)
            {
                //highest weight index for animation events
                if (bsAnimationList.weights[i] > bsHighestWeight)
                {
                    bsHighestWeight = bsAnimationList.weights[i];
                    bsAnimationList.bsHighestWeightIndex = i;
                }
            }
        }
    }
