                    Log.Message($"\nNo animation data found for pose blend space animation {poseBlendSpaceAnimation.name}\n");
                    continue;
                }

                // create layer for each animation
                //int id = meshSkinned.AddAnimation(animations[i].asset.Path);//*
                meshSkinned.AddLayer();
                if (poseBsAnimationList.layerIndex == 0)//layer index and prevLayerIndex for the animation list hasnt been set yet
