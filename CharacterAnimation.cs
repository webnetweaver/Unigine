        meshSkinned.LerpLayer(scope.layeredScopeLayerNum, scope.prevLayeredScopeLayerNum, scope.layeredScopeLayerNum, scope.prevCurLayeredBlendAmount);
    }

    private void ApplyScopeLayered(Scope scope)
    {
        if (fullBodyBlendAmount != 0.0f)
            meshSkinned.LerpLayer(scope.layerNumber, scope.layerNumber, scope.layeredScopeLayerNum, scope.layeredBlendAmount);
        else
            meshSkinned.LerpLayer(scope.layerNumber, 0, scope.layeredScopeLayerNum, scope.layeredBlendAmount);
    }

    private void AdvanceScopeFrame(Scope scope)
    {
        float frame;
        if (scope.baseAnimationName != null)
        {
            NamedAnimation scopeAnimation = GetNamedAnimation(scope.baseAnimationName, scope.animations);
            if (scopeAnimation.frameNumber == -1)
            {
                frame = ComputeScopeFrame(scope, scopeAnimation);
                meshSkinned.SetLayerFrame(scope.layerNumber, frame, scopeAnimation.from, scopeAnimation.to);
            }
            else
            {
                meshSkinned.SetLayerFrame(scope.layerNumber, scopeAnimation.frameNumber);
            }
        }
        if (scope.layeredAnimationName != null)
        {
            NamedAnimation layeredScopeAnimation = GetNamedAnimation(scope.layeredAnimationName, scope.animations);
            float delta = updateTimeDelta * scope.framesPerSecond * timeScale;
            frame = meshSkinned.GetLayerFrame(scope.layeredScopeLayerNum) + delta;
            int lastFrame = GetTotalScopeFrames(layeredScopeAnimation, scope.layeredScopeLayerNum);
            if (lastFrame > frame)
                meshSkinned.SetLayerFrame(scope.layeredScopeLayerNum, frame, layeredScopeAnimation.from, layeredScopeAnimation.to);
            else
            {
                //end of layered animation, set to the last frame and start blending out
                meshSkinned.SetLayerFrame(scope.layeredScopeLayerNum, lastFrame, layeredScopeAnimation.from, layeredScopeAnimation.to);
                scope.layeredBlendTarget = 0.0f;
            }

        }
    }

    private float ComputeScopeFrame(Scope scope, NamedAnimation scopeAnimation)
    {
        float delta = updateTimeDelta * scope.framesPerSecond * timeScale;

        if (scopeAnimation.endType == AnimationEndTypes.Loop)
            return meshSkinned.GetLayerFrame(scope.layerNumber) + delta;
        else if (scopeAnimation.endType == AnimationEndTypes.Hold)
        {
            float nextFrame = meshSkinned.GetLayerFrame(scope.layerNumber) + delta;
            int totalFrames = GetTotalScopeFrames(scopeAnimation, scope.layerNumber);
            if (nextFrame >= totalFrames)
                return totalFrames;
            else
                return nextFrame;
        }
        else//Ping pong
        {
            int totalFrames = GetTotalScopeFrames(scopeAnimation, scope.layerNumber);
            if (scope.pingPongForward)
            {
                float nextFrame = meshSkinned.GetLayerFrame(scope.layerNumber) + delta;
                if (nextFrame >= totalFrames)
                {
                    //ping pong animation type has crossed over the length of the animation
                    if (nextFrame < meshSkinned.GetLayerFrame(scope.layerNumber) + totalFrames)
                    {
                        //crossover length is less than the length of the animation
                        //reverse direction starting from the end of the animation
                        scope.pingPongForward = false;
                        return totalFrames - (nextFrame - totalFrames);
                    }
                    else
                    {
                        //crossover length is greater than the length of the animation cycle
                        //this is unlikely but possible depending on the relative animation length and configured updateTimeDelta
                        //see how many animation cycles should have occurred since the last frame
                        //this indicates how many additional "bounces" occurred after crossing the animation length
                        //an odd number of additional bounces means new frame starts at the beginning by an amount (nextframe - currentframe) % animationLength
                        //an even number of additional bounces means reverse direction starting at the end by an amount (nextframe - currentframe) % animationLength
                        float cycles = (nextFrame - meshSkinned.GetLayerFrame(scope.layerNumber)) / totalFrames;
                        int bounces = (int)MathLib.Round(cycles, MidpointRounding.ToZero);
                        if (bounces % 2 != 0)
                        {
                            //odd number of additional bounces
                            return (nextFrame - meshSkinned.GetLayerFrame(scope.layerNumber)) % totalFrames;
                        }
                        else
                        {
                            //even number of additional bounces
                            scope.pingPongForward = false;
                            return totalFrames - ((nextFrame - meshSkinned.GetLayerFrame(scope.layerNumber)) % totalFrames);
                        }
                    }
                }
                else
                    return nextFrame;
            }
            else
            {
                float nextFrame = meshSkinned.GetLayerFrame(scope.layerNumber) - scope.framesPerSecond * delta;
                if (nextFrame <= 0)
                {
                    //reversing the ping pong animation has crossed over the zero frame of the animation
                    if (nextFrame > meshSkinned.GetLayerFrame(scope.layerNumber) - totalFrames)
                    {
                        //crossover length is less than the length of the animation (nextFrame is negative)
                        //reverse direction to forward, make nextFrame positive, and start at the beginning by amount nextFrame
                        scope.pingPongForward = true;
                        return MathLib.Negative(nextFrame);
                    }
                    else
                    {
                        //crossover length is greater than the length of the animation cycle
                        //this is unlikely but possible depending on the relative animation length and configured updateTimeDelta
                        //see how many animation cycles should have occurred since the last frame
                        //this indicates how many additional "bounces" occurred after crossing the animation length
                        //an odd number of additional bounces means new frame starts at the end by an amount (nextframe - currentframe) % animationLength
                        //an even number of additional bounces means reverse direction starting from the end by an amount (nextframe - currentframe) % animationLength
                        float cycles = (nextFrame - meshSkinned.GetLayerFrame(scope.layerNumber)) / totalFrames;
                        int bounces = (int)MathLib.Round(MathLib.Negative(cycles), MidpointRounding.ToZero);
                        if (bounces % 2 != 0)
                        {
                            //odd number of additional bounces
                            return totalFrames - (MathLib.Negative(nextFrame - meshSkinned.GetLayerFrame(scope.layerNumber)) % totalFrames);
                        }
                        else
                        {
                            //even number of additional bounces
                            scope.pingPongForward = true;
                            return MathLib.Negative(nextFrame - meshSkinned.GetLayerFrame(scope.layerNumber)) % totalFrames;
                        }
                    }
                }
                else
                    return nextFrame;
            }
        }
    }

    private int GetTotalScopeFrames(NamedAnimation scopeAnimation, int layerNum)
    {
        if (scopeAnimation.from == -1 && scopeAnimation.to == -1)
            return meshSkinned.GetLayerNumFrames(layerNum);
        int startFrame = scopeAnimation.from == -1 ? 0 : scopeAnimation.from;
        int endFrame = scopeAnimation.to == -1 ? meshSkinned.GetLayerNumFrames(layerNum) : scopeAnimation.to - 1;
        return endFrame - startFrame;
    }

    private void UpdateScopeBlend(Scope scope, Boolean scopeBlending, Boolean scopeLayerBlending)
    {
        float lerpAmount;// = scope.blendSpeedFactor * updateTimeDelta * timeScale; //scope blend in speed or layered scope blend in speed
        //float delta = updateTimeDelta;
        float blendTarget;
        if (!scopeLayerBlending)//layered animation is present take the highest of the two blends for the final scope blend target
        {
            blendTarget = scope.blendTarget;
            lerpAmount = scope.blendSpeedFactor * updateTimeDelta * timeScale;
        }
        else
        {
            if (!scopeBlending)
            {
                blendTarget = scope.layeredBlendTarget;
                lerpAmount = scope.layeredBlendSpeedFactor * updateTimeDelta * timeScale;
            }
            else
            {
                //edge case: both base and layered are blending
                //take the higher of the blend targets 
                blendTarget = scope.blendTarget > scope.layeredBlendTarget ? scope.blendTarget : scope.layeredBlendTarget;
                //we're changing blend amount and have both a base and layered animation present
                //fully blend in when either of them is fully blended in, but dont blend out until theyre both blended out
                //take the higher of the speed factors if blending in and the lower of the speed factors if blending out
                if (scope.baseAnimationName == null)
                    lerpAmount = scope.layeredBlendSpeedFactor * updateTimeDelta * timeScale;
                else
                {
                    //we're changing blend amount and have both a base and layered animation present
                    //take the higher of the speed factors if blending in and the lower of the speed factors if blending out
                    float speedFactor;
                    if (blendTarget == 1.0f)
                        speedFactor = scope.blendSpeedFactor > scope.layeredBlendSpeedFactor ? scope.blendSpeedFactor : scope.layeredBlendSpeedFactor;
                    else
                        speedFactor = scope.blendSpeedFactor < scope.layeredBlendSpeedFactor ? scope.blendSpeedFactor : scope.layeredBlendSpeedFactor;
                    lerpAmount = speedFactor * updateTimeDelta * timeScale;
                }
            }
        }

        if (blendTarget > scope.blendAmount)
        {
            scope.blendAmount = (float)Unigine.MathLib.Min(scope.blendAmount + lerpAmount, 1.0f);
            if (scope.blendAmount == 1.0f && scope.layeredBlendTarget != 1.0f)
                onScopeBlendedIn.Run(scope.name);
        }
        else if (blendTarget < scope.blendAmount)
        {
            scope.blendAmount = (float)Unigine.MathLib.Max(scope.blendAmount - lerpAmount, 0.0f);
            if (scope.blendAmount == 0.0f)
            {
                //reset triggers once base animation is blended out
                //updateLayeredFullBodyBlend will call this for the layered animation
                if (scope.layeredAnimationName == null && scope.baseAnimationName != null)
                {
                    ResetTriggersForAnimation(scope.baseAnimationName, scopeAnimationTriggers);

                    meshSkinned.SetLayerFrame(scope.layerNumber, 0.0f);
                    scope.baseAnimationName = null;
                    onScopeBlendedOut.Run(scope.name);
                }
            }
        }
    }

    private void ApplyScope(Scope scope)
    {
        if (scope.blendAmount > 0)
        {
            if (scope.boneIndices.Count > 0)
            {
                if (scope.blendAmount < 1.0f)
