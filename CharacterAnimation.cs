    private void Init()
    {
        meshSkinned = node as ObjectMeshSkinned;
        if (!meshSkinned)
            return;

        firstPersonController = node.GetComponentInParent<FirstPersonController>();

        InitBlendSpaces();

        InitPoseBlendSpaces();

        InitScopes();

        InitFullBody();

        if (processType == AnimationProcessTypes.CustomRate)
        {
            if (processRate > 0)
            {
                updateTime = (float)(1.0f / processRate);
                lastUpdateTime = updateTime;//force initial animation update
            }
            else
            {
                Log.Warning($"Blend tree fps must be greater than 0");
                processType = AnimationProcessTypes.EveryFrame;
            }
        }

    }

